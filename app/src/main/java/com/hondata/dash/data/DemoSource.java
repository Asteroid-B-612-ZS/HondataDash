    // === DataSource 接口 ===

    private final Runnable tick = new Runnable() {
        @Override public void run() {
            if (!running) return;
            if (callback != null) callback.onDataReceived(generate());
            uiHandler.postDelayed(this, FRAME_MS);
        }
    };

    @Override public String getName() { return "Demo (LOG回放)"; }
    @Override public void setCallback(Callback cb) { this.callback = cb; }
    @Override public boolean isConnected() { return connected; }

    @Override
    public void connect(String address) {
        connected = true;
        startTime = System.currentTimeMillis();
        uiHandler.post(new Runnable() {
            @Override public void run() {
                if (callback != null) callback.onConnected();
            }
        });
    }

    @Override
    public void disconnect() {
        running = false;
        connected = false;
        uiHandler.removeCallbacks(tick);
        uiHandler.post(new Runnable() {
            @Override public void run() {
                if (callback != null) callback.onDisconnected();
            }
        });
    }

    @Override
    public void startPolling() {
        running = true;
        uiHandler.post(tick);
    }

    @Override
    public void stopPolling() {
        running = false;
        uiHandler.removeCallbacks(tick);
    }

    // === 回放引擎 ===

    /**
     * 在关键帧数组中线性插值
     */
    private float[] lerp(float[][] seg, float tMs) {
        int n = seg.length;
        if (n == 0) return new float[KF_COLS];
        if (n == 1 || tMs <= seg[0][T]) return seg[0].clone();
        if (tMs >= seg[n - 1][T]) return seg[n - 1].clone();

        // 二分查找包围帧
        int lo = 0, hi = n - 1;
        while (hi - lo > 1) {
            int mid = (lo + hi) >>> 1;
            if (seg[mid][T] <= tMs) lo = mid;
            else hi = mid;
        }

        float[] a = seg[lo], b = seg[hi];
        float dt = b[T] - a[T];
        float frac = dt > 0 ? (tMs - a[T]) / dt : 0;

        float[] result = new float[KF_COLS];
        for (int i = 0; i < KF_COLS; i++) {
            result[i] = a[i] + (b[i] - a[i]) * frac;
        }
        return result;
    }

    /**
     * 生成当前帧数据
     */
    private SensorData generate() {
        SensorData d = new SensorData();
        d.timestamp = System.currentTimeMillis();

        long elapsed = (System.currentTimeMillis() - startTime) % CYCLE;

        float[] f;
        if (elapsed < REAL_TOTAL) {
            // 真实 LOG 回放: 定位段 + 插值
            long t = elapsed;
            float[][] seg;
            if (t < SEG_DUR[0]) {
                seg = IDLE;
            } else if ((t -= SEG_DUR[0]) < SEG_DUR[1]) {
                seg = NORMAL;
            } else if ((t -= SEG_DUR[1]) < SEG_DUR[2]) {
                seg = WOT;
            } else if ((t -= SEG_DUR[2]) < SEG_DUR[3]) {
                seg = DFCO;
            } else {
                t -= SEG_DUR[3];
                seg = ACCEL;
            }
            f = lerp(seg, t);
        } else {
            // EXTREME: 合成正弦波扫满 ScaleBar 量程
            f = generateExtreme(elapsed - T_EXTREME);
        }

        // === 映射到 SensorData PIDs ===
        float mapVal = f[MAP];
        float baro = 101.0f;

        d.put(0x100, f[RPM]);              // RPM
        d.put(0x101, f[VSS]);              // VSS
        d.put(0x102, 3);                   // Gear (estimated)
        d.put(0x110, mapVal);              // MAP kPa
        d.put(0x112, mapVal * 0.8);        // MAP filtered
        d.put(0x114, Math.max(0, (mapVal - baro) / 100.0)); // Boost bar
        d.put(0x170, baro);                // Baro kPa
        d.put(0x120, f[TP]);               // TP Pedal %
        d.put(0x122, f[TP]);               // TP Plate %
        d.put(0x150, f[IAT] - 1);          // IAT raw
        d.put(0x151, f[IAT]);              // IAT °C
        d.put(0x160, f[ECT]);              // ECT °C
        d.put(0x161, f[ECT] + 1);          // ECT raw
        d.put(0x140, f[IGN]);              // IGN °
        d.put(0x130, f[INJ]);              // Injector ms
        d.put(0x132, f[INJ] * 10);         // Injector related
        d.put(0x320, f[LAM]);              // Lambda (AF)
        d.put(0x329, f[TGL]);              // AFCMD
        d.put(0x330, f[STRIM]);            // S.TRIM %
        d.put(0x332, f[LTRIM]);            // L.TRIM %
        d.put(0x180, f[BAT]);              // Battery V
        d.put(0x116, f[FP]);               // Fuel Pressure
        d.put(0x412, f[KCTRL]);            // Knock Control
        d.put(0x410, f[KRET]);             // Knock Retard
        d.put(0x411, f[KLEVEL]);           // Knock Level
        d.put(0x421, f[KC1]);              // Knock Cyl 1
        d.put(0x422, f[KC2]);              // Knock Cyl 2
        d.put(0x423, f[KC3]);              // Knock Cyl 3
        d.put(0x424, f[KC4]);              // Knock Cyl 4
        d.put(0xB03, f[ETH]);              // Ethanol %
        d.put(0xB04, f[ETH] + 15);         // Ethanol related
        d.put(0x191, f[FP]);               // Fuel Pressure
        d.put(0x192, f[FP] * 0.6);         // FP related
        d.put(0x1A0, f[WG]);               // WG Duty %
        d.put(0x1A1, f[WG]);               // WG Actual %

        // V2.0: 引擎状态检测所需 PIDs
        d.put(0x0340, f[CL]);              // ClosedLoop
        d.put(0x0322, f[TGL]);             // TargetLambda

        return d;
    }

    /**
     * EXTREME: 正弦波扫满全部 ScaleBar 量程 (9s)
     */
    private float[] generateExtreme(long tMs) {
        float t = (float) tMs / (CYCLE - T_EXTREME); // 0~1
        float s = (float) Math.sin(t * Math.PI * 4);  // -1~+1, 4 周期
        float h = 0.5f + 0.5f * s;                     // 0~1

        float[] f = new float[KF_COLS];
        f[T] = tMs;
        f[RPM] = 3000 + 3000 * h;
        f[VSS] = 60 + 40 * s;
        f[MAP] = 150 + 150 * s;         // 0~300 kPa
        f[TP] = 50 + 40 * s;
        f[IAT] = 60 + 40 * s;           // 20~100
        f[ECT] = 80 + 40 * s;           // 40~120
        f[INJ] = 3 + 3 * h;
        f[IGN] = 40 * s;                // -40~40
        f[LAM] = 0.915f + 0.306f * s;   // 0.61~1.22 (Lambda)
        f[STRIM] = 25 * s;              // -25~25
        f[LTRIM] = 25 * s;
        f[ETH] = 50 + 50 * s;           // 0~100
        f[WG] = 30 + 30 * h;
        f[FP] = 350 + 200 * h;
        f[BAT] = 13.5f;
        f[KCTRL] = 30 * h;
        f[KRET] = 3 * h;
        f[KLEVEL] = 10 + 5 * h;
        f[KC1] = (int) (3 * h);
        f[KC2] = f[KC1];
        f[KC3] = f[KC1];
        f[KC4] = f[KC1];
        f[CL] = 1.0f;                   // Closed loop
        f[TGL] = 1.0f;                  // Stoich target
        return f;
    }
